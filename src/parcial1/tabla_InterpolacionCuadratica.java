    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        x= Double.parseDouble(jTextField1.getText());
        x0= Double.parseDouble(jTextField2.getText());
        x1= Double.parseDouble(jTextField3.getText());
        x2= Double.parseDouble(jTextField8.getText());
        f2_x= Double.parseDouble(jTextField4.getText());
        f2_x0= Double.parseDouble(jTextField5.getText());
        f2_x1= Double.parseDouble(jTextField6.getText());
        f2_x2= Double.parseDouble(jTextField9.getText());
        
        b0= f2_x0;
        b1= objeto.RedoNumero((f2_x1 - f2_x0)/(x1 - x0));
        b2= objeto.RedoNumero((f2_x2 - f2_x0 - (b1 * (x2 - x0))) / ((x2 - x0) * (x2 - x1)));
        
        f2_x_= objeto.RedoNumero(b0 + (b1 * (x - x0)) + (b2 * ((x - x0) * (x - x1))));
        ep= objeto.RedoNumero((Math.abs((f2_x_ - f2_x) / f2_x_)) * 100);
        jTextField7.setText("" + ep);
    }//GEN-LAST:event_jButton1ActionPerformed

